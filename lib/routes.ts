export function cityPath(slug: string) {
  return `/ciudades/${slug}`;
}

export function routePath(slug: string) {
  return `/rutas/${slug}`;
}

export function highwayPath(slug: string) {
  return `/autopistas/${slug}`;
}

export function pricePath(slug: string) {
  return `/precios/${slug}`;
}

export function blogPath(slug: string) {
  return `/blog/${slug}`;
}
